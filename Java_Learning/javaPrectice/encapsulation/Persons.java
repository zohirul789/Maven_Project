package encapsulation;

public class Persons {
	
	private String name;
	private int age;
	private String email;
	
	public String GetName() {
		return name;
	}
	public void SetName(String name) {
		this.name = name;
	}
	public int GetAge() {
		return age;
	}
	public void SetAge(int age) {
		this.age = age;
	}
	public String GetEmail() {
		return email;
		
	}
	public void SetEmail(String email) {
		this.email = email;
	}

}
